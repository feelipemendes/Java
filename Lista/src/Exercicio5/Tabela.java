public class Tabela {
    public static void main(String[] args) {
        System.out.println("Tabela de Multiplicação: ");
        for(int i = 1; i <= 10; i++){
            for(int j = 1; j <= 10; j++){
                System.out.println(i+" x "+j+" = "+(i*j));
                
            }
        }
        
    }
    
}
